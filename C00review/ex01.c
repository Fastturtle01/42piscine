#include <unistd.h>

int	main(void)
{
	int	i;
	int	j;
	int	k;

	i = '0';
	while (i <= '9')
	{
		j = '0';
		while (j <= '9')
		{
			k = '0';
			while (k <= '9')
			{
				if (i < j && j <k)
				{
					write(1, &i, 1);
					write(1, &j, 1);
					write(1, &k, 1);
					if (i != '7' || j != '8' || k != '9')
						write(1,", ", 2);
				}
				k++;
			}
			j++;
		}
		i++;
	}	
}
